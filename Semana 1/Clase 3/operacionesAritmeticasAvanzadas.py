# Division normal
print(40 / 13)

# divisiones enteras (Se quitan los desimales)
divisionEntera = 40 // 13
print(divisionEntera)

# divisiones de residuo
divisionDeResiduo = 40 % 13
print(divisionDeResiduo)

# Residuo de un numero par
residuoNumeroPar = 10 % 2
print(residuoNumeroPar)

# Residuo de un numero impar
residuoNumeroImpar = 17 % 2
print(residuoNumeroImpar)

# Potenciaci√≥n
potenciacion = 8 ** 2
print(potenciacion)