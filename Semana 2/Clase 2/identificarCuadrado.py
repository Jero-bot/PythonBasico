# Pedirle al usuario el alto y el ancho de un rectangulo y decirle si es cuadrado
alto = int(input("Por favor ingrese el alto del rectangulo: "))

ancho = int(input("Por favor ingrese el ancho del rectangulo: "))

if alto == ancho:
    print("Tu rectangulo es cuadrado")
else:
    print("Tu rectangulo no es cuadrado")
